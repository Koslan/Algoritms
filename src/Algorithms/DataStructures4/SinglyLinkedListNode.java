package Algorithms.DataStructures4;

public class SinglyLinkedListNode {

    int link;
    int value;

    public SinglyLinkedListNode(int link, int value) {
        this.link = link;
        this.value = value;
    }

    public int getLink() {
        return link;
    }

    public int getValue() {
        return value;
    }
}
